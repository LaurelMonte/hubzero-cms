	/**
	 * Populates the object using an array.
	 *
	 * This is particularly useful when populating an object from one of the
	 * request arrays (e.g. $_POST).  This method goes through the column
	 * names, checking to see whether a matching key exists in populated
	 * array. If so the setByName() method is called for that column.
	 *
	 * You can specify the key type of the array by additionally passing one
	 * of the class type constants TYPE_PHPNAME, TYPE_COLNAME, TYPE_FIELDNAME,
	 * TYPE_NUM. The default key type is the column's phpname (e.g. 'authorId')
	 *
	 * @param      array  $arr     An array to populate the object from.
	 * @param      string $keyType The type of keys the array uses.
	 * @return     void
	 */
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = NCMessagePeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setId($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setDescription($arr[$keys[1]]);
		if (array_key_exists($keys[2], $arr)) $this->setKeyName($arr[$keys[2]]);
		if (array_key_exists($keys[3], $arr)) $this->setKeyValue($arr[$keys[3]]);
		if (array_key_exists($keys[4], $arr)) $this->setLanguage($arr[$keys[4]]);
		if (array_key_exists($keys[5], $arr)) $this->setNameSpace($arr[$keys[5]]);
		if (array_key_exists($keys[6], $arr)) $this->setShortLanguage($arr[$keys[6]]);
	}

	/**
	 * Build a Criteria object containing the values of all modified columns in this object.
	 *
	 * @return     Criteria The Criteria object containing all modified values.
	 */
	public function buildCriteria()
	{
		$criteria = new Criteria(NCMessagePeer::DATABASE_NAME);

		if ($this->isColumnModified(NCMessagePeer::ID)) $criteria->add(NCMessagePeer::ID, $this->id);
		if ($this->isColumnModified(NCMessagePeer::DESCRIPTION)) $criteria->add(NCMessagePeer::DESCRIPTION, $this->description);
		if ($this->isColumnModified(NCMessagePeer::KEY_NAME)) $criteria->add(NCMessagePeer::KEY_NAME, $this->key_name);
		if ($this->isColumnModified(NCMessagePeer::KEY_VALUE)) $criteria->add(NCMessagePeer::KEY_VALUE, $this->key_value);
		if ($this->isColumnModified(NCMessagePeer::LANGUAGE)) $criteria->add(NCMessagePeer::LANGUAGE, $this->language);
		if ($this->isColumnModified(NCMessagePeer::NAME_SPACE)) $criteria->add(NCMessagePeer::NAME_SPACE, $this->name_space);
		if ($this->isColumnModified(NCMessagePeer::SHORT_LANGUAGE)) $criteria->add(NCMessagePeer::SHORT_LANGUAGE, $this->short_language);

		return $criteria;
	}

	/**
	 * Builds a Criteria object containing the primary key for this object.
	 *
	 * Unlike buildCriteria() this method includes the primary key values regardless
	 * of whether or not they have been modified.
	 *
	 * @return     Criteria The Criteria object containing value(s) for primary key(s).
	 */
	public function buildPkeyCriteria()
	{
		$criteria = new Criteria(NCMessagePeer::DATABASE_NAME);

		$criteria->add(NCMessagePeer::ID, $this->id);

		return $criteria;
	}

